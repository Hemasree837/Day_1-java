import java.util.*;
public class AcceptingString
{
    public static void main(String args[])
    {
        //Accepting a string
        Scanner sc=new Scanner(System.in);
        String a=sc.nextLine();
        String b=sc.nextLine();
        String c=sc.nextLine();
        System.out.print(a);
        System.out.print(b);
        System.out.print(c);

    }
}